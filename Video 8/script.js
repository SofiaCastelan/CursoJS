var color = prompt("Ingrese un color")

switch(color){

    case "rojo":
        console.log("es rojo");
        break;

    case "azul":
        console.log("es azul");
        break;
    
    case "amarillo":
        console.log("es amarillo");
        break;

    default:
        console.log("El color no es valido");

}